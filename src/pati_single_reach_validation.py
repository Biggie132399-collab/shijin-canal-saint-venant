def run(out_dir: Path = OUT_DIR) -> Dict[str, float]:
    ch = TrapezoidChannel()
    cfg = RunConfig()
    out_dir.mkdir(parents=True, exist_ok=True)
    fig_dir = out_dir / "figures"
    fig_dir.mkdir(parents=True, exist_ok=True)
    n_steps = int(round(cfg.duration_hours * 3600.0 / cfg.dt_seconds)) + 1
    times_h = [i * cfg.dt_seconds / 3600.0 for i in range(n_steps)]
    y_head = upstream_depth_boundary(times_h, cfg)
    q_head = discharge_series(y_head, ch, cfg.dt_seconds)
    forward = forward_generate_boundary(y_head, q_head, ch, cfg)
    reverse = reverse_reconstruct(forward["depth_nodes"][-1], forward["discharge_nodes"][-1], ch, cfg)
    y_sim = reverse["depth_nodes_upstream_first"][0]
    q_sim = reverse["discharge_nodes_upstream_first"][0]
    summary = {
        "depth_rmse_m": rmse(y_sim, y_head),
        "depth_nse": nse(y_sim, y_head),
        "discharge_rmse_m3s": rmse(q_sim, q_head),
        "discharge_nse": nse(q_sim, q_head),
        "depth_peak_time_error_h": peak_time_error(y_sim, y_head, times_h),
        "discharge_peak_time_error_h": peak_time_error(q_sim, q_head, times_h),
        "max_abs_depth_error_m": max(abs(a - b) for a, b in zip(y_sim, y_head)),
        "max_abs_discharge_error_m3s": max(abs(a - b) for a, b in zip(q_sim, q_head)),
    }
    draw_chart(
        fig_dir / "fig1_single_reach_stage_reconstruction.png",
        "图1  单一梯形渠段上游水深反算验证",
        times_h,
        [
            ("下游已知水深 yd", forward["depth_nodes"][-1], "#0B5CAD", "solid"),
            ("上游基准水深 yu,obs", y_head, "#2A9D8F", "solid"),
            ("RSR反推水深 yu,sim", y_sim, "#C75146", "solid"),
        ],
        "时间 t (h)",
        "水深 y (m)",
        "下游水深由同一组Pati型方程正向生成；红线与绿线重合表示反向stage routing可闭合。",
    )
    draw_chart(
        fig_dir / "fig2_single_reach_discharge_reconstruction.png",
        "图2  单一梯形渠段上游流量反算验证",
        times_h,
        [
            ("下游已知流量 Qd", forward["discharge_nodes"][-1], "#0B5CAD", "solid"),
            ("上游基准流量 Qu,obs", q_head, "#2A9D8F", "solid"),
            ("RSR反推流量 Qu,sim", q_sim, "#C75146", "solid"),
        ],
        "时间 t (h)",
        "流量 Q (m³/s)",
        "流量由Pati Eq. (12)型动态stage-discharge关系计算，未直接套用Manning单值关系。",
    )
    draw_rating_loop(fig_dir / "fig3_single_reach_dynamic_rating_loop.png", y_head, q_head, y_sim, q_sim, ch)
    snap_i = min(range(len(times_h)), key=lambda i: abs(times_h[i] - cfg.pulse_center_hours))
    x_nodes = [i * ch.dx_m for i in range(len(forward["depth_nodes"]))]
    draw_spatial_snapshot(
        fig_dir / "fig4_single_reach_spatial_snapshot.png",
        x_nodes,
        [node[snap_i] for node in forward["depth_nodes"]],
        [node[snap_i] for node in reverse["depth_nodes_upstream_first"]],
        [node[snap_i] for node in forward["discharge_nodes"]],
        [node[snap_i] for node in reverse["discharge_nodes_upstream_first"]],
        times_h[snap_i],
    )
    write_outputs(out_dir, times_h, forward, reverse, summary, ch, cfg)
    return summary


