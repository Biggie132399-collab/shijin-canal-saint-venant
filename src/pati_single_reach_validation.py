#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
Single-reach validation of the Pati et al. (2023) reverse-stage routing idea.

This script is deliberately independent from the 0-456 canal dispatch model.
It answers one narrow question:

    Under simple and physically consistent conditions, can a Pati-type
    reverse-stage routing discretization recover the upstream hydrograph
    from the downstream stage boundary?

Assumptions:
1. one prismatic trapezoidal channel;
2. no lateral inflow/outflow;
3. downstream stage is generated by the forward counterpart of the same
   Pati-type stage-gradient equation, rather than drawn by hand;
4. discharge is computed with a Pati Eq. (12)-type dynamic stage-discharge
   relationship, not by directly applying a static Manning rating curve.
"""

from __future__ import annotations

import csv
import json
import math
from dataclasses import asdict, dataclass
from pathlib import Path
from typing import Dict, List, Sequence, Tuple

from PIL import Image, ImageDraw, ImageFont


ROOT = Path(__file__).resolve().parents[1]
OUT_DIR = ROOT / "results" / "pati_single_reach_validation_results"


@dataclass
class TrapezoidChannel:
    length_m: float = 5000.0
    dx_m: float = 250.0
    bottom_width_m: float = 20.0
    side_slope_h_per_v: float = 1.0
    bed_slope: float = 0.0008
    manning_n: float = 0.025
    lateral_flow_m2s: float = 0.0
    routing_celerity_factor: float = 1.50


@dataclass
class RunConfig:
    dt_seconds: float = 300.0
    duration_hours: float = 18.0
    base_depth_m: float = 2.00
    pulse_amplitude_m: float = 0.25
    pulse_center_hours: float = 8.0
    pulse_sigma_hours: float = 2.5


