def run() -> Dict[str, float]:
    OUT_DIR.mkdir(parents=True, exist_ok=True)
    FIG_DIR.mkdir(parents=True, exist_ok=True)
    fcfg = ForwardConfig()
    rcfg = RSRConfig()
    forward = run_forward_saint_venant(fcfg)
    reverse = reverse_stage_routing(forward, rcfg, use_sv_tail_q=True)
    times_h: List[float] = forward["times_h"]  # type: ignore[assignment]
    depth_t: List[List[float]] = forward["depth_nodes"]  # type: ignore[assignment]
    q_t: List[List[float]] = forward["q_nodes"]  # type: ignore[assignment]
    depth_r: List[List[float]] = reverse["depth_nodes_upstream_first"]  # type: ignore[assignment]
    q_r: List[List[float]] = reverse["q_nodes_upstream_first"]  # type: ignore[assignment]
    head_h_obs = [row[0] for row in depth_t]
    head_q_obs = [row[0] for row in q_t]
    summary: Dict[str, float] = {
        "head_depth_rmse_m": rmse(depth_r[0], head_h_obs),
        "head_depth_nse": nse(depth_r[0], head_h_obs),
        "head_q_rmse_m3s": rmse(q_r[0], head_q_obs),
        "head_q_nse": nse(q_r[0], head_q_obs),
        "head_depth_peak_time_error_h": peak_time_error(depth_r[0], head_h_obs, times_h),
        "head_q_peak_time_error_h": peak_time_error(q_r[0], head_q_obs, times_h),
        "max_abs_head_depth_error_m": max(abs(a - b) for a, b in zip(depth_r[0], head_h_obs)),
        "max_abs_head_q_error_m3s": max(abs(a - b) for a, b in zip(q_r[0], head_q_obs)),
        "forward_dt_seconds": fcfg.dt_seconds,
        "rsr_dt_seconds": fcfg.output_interval_seconds,
        "length_m": max(forward["x"]),  # type: ignore[arg-type]
        "node_count": len(forward["ids"]),  # type: ignore[arg-type]
    }
    draw_two_panel(
        FIG_DIR / "fig1_head_reconstruction_sv_vs_pati_rsr.png",
        "图1  0-456真实主干渠无分水口：渠首正演-反演对比",
        times_h,
        head_h_obs,
        depth_r[0],
        head_q_obs,
        q_r[0],
        summary,
    )
    draw_spatial_error(FIG_DIR / "fig2_spatial_reconstruction_error.png", forward, reverse)
    write_csv_outputs(forward, reverse, summary)
    return summary


